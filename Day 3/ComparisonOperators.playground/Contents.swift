import UIKit

let firstScore = 6
let secondScore = 4

// Operators that check for equality: '==' -> checks two values are the same
//                                    '!=' -> checks two values are not the same

firstScore == secondScore
firstScore != secondScore

// There are four operators for comparing whether one value is greater than, less that, or
// equal to another. These are just like in mathematics

firstScore < secondScore
firstScore >= secondScore
secondScore <= firstScore
secondScore > firstScore

// Each of thse alson work with strings, because strings have a natural alphabetical order
"Taylor" <= "Swift"
