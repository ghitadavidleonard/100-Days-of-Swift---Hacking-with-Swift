import UIKit

// If you need a specific, fixed collection of related values, you should use a tuple
let address = (house: 555, street: "Taylor Swift")

// If you need a collection of values that must be unique or you need to be able to check whether
// a specific item is in there extremly quickly, you should use a set
let set = Set(["aardvark", "astronaut", "azalea"])

// If you need a collection of values that can contain duplicates, or the order of your items
// matters, use an array
let pythons = ["Eric", "Graham", "John", "Michael", "Terry", "Terry"]

